#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int		i;
	char	chr;

	i = --ft_strlen(str);
	while (i >= 0)
	{
		if (str[i] == c)
			return ((char *)&str[i]);
		i--;
	}
	return (0);
}
